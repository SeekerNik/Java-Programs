//Constant variable are the variable which will never change their value throughout the program
//their return type is final
//in java constant variable is alwayas written in capital letter
class Constants
{
	public static final String LIGHT="ggg";
	public static final double PI=55.52;
}

class ConstantVar
{
	public static void main(String arg[])
	{
	System.out.println(Constants.LIGHT);
	System.out.println(Constants.PI);

	}
}